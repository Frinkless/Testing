package org.example.class7;

public class E13ArrayLoop {
    public static void main(String[] args) {
        String [] animals={"Leon", "Elephant", "Crocodile", "Dolphin", "Giraffe"};
        int i=0;
        while( i< animals.length){
            System.out.println(animals[i]);
            i++;
        }
    }
}
