package org.example.class7;

public class E12ArrayLoop {
    public static void main(String[] args) {
        String[] cars = {"Toyota", "Range Rover", "BMW", "Mercedes", "Tesla", "Lexus" };
        int i = 0;
        while( i < 6) {
            System.out.println(cars[i]);
            i++;

        }
    }
}