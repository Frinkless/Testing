package org.example.youtube;

public class YouTube2Casting {
    public static void main(String[] args) {
        // implicit casting/automatic/widening casting from smaller data type to bigger data type
        // explicit casting/manual/narrowing casting from bigger data type to smaller data type
        int d1=5;
        double d2=d1;
        System.out.println(d2);
        double i1=4.5;
        int i2=(int)i1;
        System.out.println(i2);



    }
}
