package org.example.class2;

public class E4CreatingVariablesAndPrinting {
    public static void main(String[] args) {
        String asterisk=("*");
        System.out.println(asterisk);
        String asterisk2=("**");
        System.out.println(asterisk2);
        String asterisk3=("***");
        System.out.println(asterisk3);
        String pound2=("##");
        System.out.println(pound2);
        String pound=("#");
        System.out.println(pound);
    }
}
