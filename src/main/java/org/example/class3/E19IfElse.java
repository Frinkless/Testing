package org.example.class3;

public class E19IfElse {
    public static void main(String[] args) {
        int age=16;
        if(age>=18){
            System.out.println("You can vote");
        }else {
            System.out.println("You can't vote");
        }
        //if the condition age>=18 is true, it will print 1st if statement
        //if the condition age>=18 is false, it will print 2nd else statement
    }
}
