public class E16IfElse {
    public static void main(String[] args) {
        double bankBalance=120000;
        if(bankBalance>80000){
            System.out.println("Lets buy a Tesla");
            //  if condition is true and result will be printed
            // if condition is not true result will not be printed
        }
        //Only whatever condition in the curly bracket will be printed
       }
}
