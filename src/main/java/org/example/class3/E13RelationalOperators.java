package org.example.class3;

public class E13RelationalOperators {
    public static void main(String[] args) {

        int num1=100;
        int num2=20;
        boolean r=num1>num2;
        //above we are storing the result in boolean and then printing
        //below we are printing the result, without storing the result
        System.out.println(num1>num2);
        System.out.println(r);

        int num3=10;
        int num4=20;
        boolean r2=num3>num4;
        //above we are storing the result in boolean and then printing
        //below we are printing the result, without storing the result
        System.out.println(num3>num4);
        System.out.println(r2);
        
    }
}
