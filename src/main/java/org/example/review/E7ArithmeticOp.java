package org.example.review;

public class E7ArithmeticOp {
    public static void main(String[] args) {
        double num1=10;
        double num2=3;
        double result=num1/num2;
        System.out.println(result);
        int num3=10;
        int num4=3;
        int result2=num3/num4;
        System.out.println(result2);
        // when you use int datatype you will be getting result in int datatype
        // when you change the datatype to double, you will get the result in double datatype
        int num5=10;
        int num6=3;
        int remainder=num5%num6;
        System.out.println(remainder);
        // meaning 10/3 is equal to 3 and remainder is 1
    }
}
