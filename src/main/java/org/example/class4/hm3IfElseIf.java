package org.example.class4;

public class hm3IfElseIf {
    public static void main(String[] args) {
        int num=5;
        if(num%2==0){
            System.out.println("Even");
        }else{
            System.out.println("Odd");
        }
        // to find if the number is even or odd
        // we are using division
        // if the division is 0 then it is even
        //when you divide even number by 2 remainder% will be zero
    }
}
